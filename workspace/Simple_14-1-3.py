class Drink:
    def get_name(self):
        return self.name
    def get_price(self):
        return self.price

drink_1 = Drink()
drink_1.name = "coffee"
drink_1.price = 100

n_1 = drink_1.get_name()
p_1 = drink_1.get_price()
print(n_1, "價格為", p_1, "元整")

